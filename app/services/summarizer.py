import anthropic
import json
from app.config import ANTHROPIC_API_KEY

client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)

def _call_claude(prompt: str, max_tokens: int = 2000) -> str:
    message = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=max_tokens,
        messages=[{"role": "user", "content": prompt}]
    )
    response = message.content[0].text.strip()

    # Strip markdown code fences
    response = response.replace("```json", "").replace("```", "").strip()

    # Extract only the first valid JSON object
    # This handles cases where Claude adds extra text after the JSON
    
    brace_count = 0
    json_start = None
    json_end = None

    for i, char in enumerate(response):
        if char == "{":
            if json_start is None:
                json_start = i
            brace_count += 1
        elif char == "}":
            brace_count -= 1
            if brace_count == 0 and json_start is not None:
                json_end = i + 1
                break

    if json_start is not None and json_end is not None:
        response = response[json_start:json_end]

    return response


def summarize_recipe(transcript: str) -> dict:
    prompt = f"""You are a culinary assistant. Extract the recipe from this video transcript.

Transcript:
{transcript}

Respond in this exact JSON format and nothing else:
{{
  "title": "<recipe name>",
  "description": "<one line description of the dish>",
  "ingredients": ["<ingredient 1 with quantity>", "<ingredient 2 with quantity>"],
  "instructions": ["<step 1>", "<step 2>", "<step 3>"],
  "tips": ["<any tips or variations mentioned>"],
  "serving_size": "<how many people it serves if mentioned>"
}}"""

    try:
        response = _call_claude(prompt)
        return {"success": True, "content_type": "recipe", "output": json.loads(response)}
    except Exception as e:
        return {"success": False, "error": str(e)}


