def summarize(transcript: str, content_type: str) -> dict:
    """
    Master router — calls the right summarizer based on content type.
    """
    router = {
        "recipe": summarize_recipe,
        "coding": summarize_coding,
        "educational": summarize_educational,
        "music": summarize_music,
        "general": summarize_general,
    }

    fn = router.get(content_type, summarize_general)
    return fn(transcript)